/**
 * Базовый класс для модулей аналитики
 * Все модули должны следовать этой структуре
 */
const BaseModuleTemplate = {
    /**
     * Уникальный ID модуля (ОБЯЗАТЕЛЬНО)
     * Должен совпадать с module_id из backend
     */
    id: null,

    /**
     * Переводы метрик модуля на русский язык
     * Ключ - название метрики из summary, значение - перевод
     *
     * НЕ переводить технические термины: ROI, CR, CPA, CPL, CTR
     *
     * Пример:
     * {
     *     total_losses: 'Общие убытки',
     *     critical_count: 'Критических',
     *     avg_bad_streak: 'Средняя длительность слива'
     * }
     */
    translations: {},

    /**
     * Описание алгоритма работы модуля (HTML)
     * Отображается во вкладке "О модуле" -> "Алгоритм работы"
     *
     * Формат: <ol><li>Шаг 1</li><li>Шаг 2</li>...</ol>
     *
     * Пример:
     * `<ol>
     *     <li>Анализируется статистика кампаний за последние 3 дня</li>
     *     <li>Фильтруются кампании с расходом более $5</li>
     *     <li>Вычисляется средний ROI для каждой кампании</li>
     * </ol>`
     */
    algorithm: null,

    /**
     * Описание используемых метрик (HTML)
     * Отображается во вкладке "О модуле" -> "Используемые метрики"
     *
     * Формат: <li><strong>Метрика</strong> - описание</li>...
     *
     * Пример:
     * `<li><strong>ROI</strong> - возврат инвестиций, %</li>
     *  <li><strong>Cost</strong> - затраты на рекламу, $</li>`
     */
    metrics: null,

    /**
     * Переводы названий параметров для отображения в UI
     * Используется в renderRunParams() для отображения параметров запуска
     *
     * Пример:
     * {
     *     min_spend: 'Минимальный расход',
     *     days: 'Период анализа',
     *     roi_threshold: 'Порог ROI'
     * }
     */
    paramTranslations: {},

    /**
     * Кастомная отрисовка таблицы результатов (опционально)
     * Если null - используется стандартная отрисовка
     *
     * @param {Object} results - Результаты модуля
     * @param {HTMLElement} container - Контейнер для таблицы
     *
     * Используется только для модулей с нестандартной структурой данных
     * Например: microtrend_scanner (positive_trends + negative_trends)
     */
    renderTable: null,

    /**
     * Кастомная отрисовка графиков (опционально)
     * Если null - используется стандартная отрисовка
     *
     * @param {Object} results - Результаты модуля
     * @param {HTMLElement} container - Контейнер для графиков
     */
    renderCharts: null,

    /**
     * Кастомная отрисовка summary (опционально)
     * Если null - используется стандартная отрисовка
     *
     * @param {Object} results - Результаты модуля
     * @param {HTMLElement} container - Контейнер для summary
     */
    renderSummary: null
};
